# comparision operators

# equal to ==  ,  not equal to != , grether than > , less than <
a,b = 30,24


# equal to ==
print('ret - F :',a == b)


# Not equal to !=
print('ret - T :',a != b)

# grether than > 
print('ret - T :',a>b)

# less than <
print('ret - F :',a<b)

# grether then or equal >=
print('ret - T :',a>=b)

# less then or equal <=
print('ret - F :',a<=b)